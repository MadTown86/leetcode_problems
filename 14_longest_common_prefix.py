from collections import Counter
class Solution:
    def longestCommonPrefix(self, strs: list[str]) -> str:
        # Time complexity - min(strs) searches O(n) once then O(n) to loop through all but last character?
        i = 0
        word = strs.pop(0)
        if '' in strs:
            return ''
        while i <= len(word):
            for item in strs:

                if item[:i] != word[:i]:
                    return word[:i-1]
            i += 1
        if i == len(word):
            return word
        else:
            return word[:i]

    def longestCommonPrefix2(self, strs: list[str]) -> str:
        # Time Complexity - worst case O(n) / best case O(n)? (words in strs * chars in words * unique index chars)?
        # Memory Complexity - Every uniquely placed digit, I believe worst case would be O(n) size of strs
        C = Counter()
        res = ''
        ind_list = []
        for word in strs:
            for char, ind in enumerate(word):
                C[(char, ind)] += 1

        ind_list = [x for (x, y) in C if C[(x, y)] == len(strs)]
        i = 0
        while True and ind_list:
            if i == len(ind_list):
                return strs[0][:i]
            if ind_list[i] != i:
                return strs[0][:i]
            i += 1

        return ''

